package practice.part1;

public class My_Java {

    public static void main(String[] args) {
        System.out.println("Variables and data types");

        int age = 56;
        System.out.println("Age: " + age + ".");
        age = 45;
        System.out.println("Age: " + age);
        byte age1 = 25;
        System.out.println("Age1: " + age1);
        short num = 129;
        System.out.println("Number: " + num);
        int num1 = 12345;
        System.out.println("Number1: " + num1);
        long num2 = 123456;
        System.out.println("Number2: " + num2);
        float num3 = 4.5f;
        System.out.println("Number3: " + num3);
        double num4 = 5.323;
        System.out.println("Number4: " + num4);
        char ch = 'v';
        System.out.println("Symbol: " + ch);
        String user_name = "Hello World!";
        System.out.println("Sentences: " + user_name);
        boolean isHappy = true; // or false
        System.out.println("I am happy: " + isHappy);
    }
}
